
//letter grade

var marks = prompt('Enter your marks:');
if(marks>=80){
    console.log('You got A+');
}
else if(marks>=70){
    console.log('You got A');
}
else if(marks>=60){
    console.log('You got -A');
}
else if(marks>=50){
    console.log('You got B');
}
else if(marks>=40){
    console.log('You got C');
}
else if(marks>=33){
    console.log('You got D');
}
else{
    console.log('Fail')
}